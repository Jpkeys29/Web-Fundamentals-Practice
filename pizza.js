function pizzaOven (crustType, sauceType, cheeses, toppings){
    var pizza = {
    crustType : crustType,
    sauceType : sauceType,
    cheeses : cheeses,
    toppings : toppings,
    }
    return pizza
}
console.log(pizzaOven ("thin", "pesto", "provole", "prosciuto"))
console.log(pizzaOven ("deep dish", "traditional", "mozzarella", "pepperoni"))
console.log(pizzaOven ("hand tossed", "marinara", "mozzarella", "feta"))

    






